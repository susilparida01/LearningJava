package com.greatlearning.codingpracticeone;
/*
Write a java program to calculate the sum and product of 2 decimal numbers
 */

public class SumAndProductOf2DecimalNumber {
    public static void main(String[] args) {
        double firstVariable, secondVariable;
        firstVariable = 20.45;
        secondVariable = 30.35;
        System.out.println("Sum is "+(firstVariable+secondVariable));
        System.out.println("Product is "+(firstVariable*secondVariable));
    }
}
