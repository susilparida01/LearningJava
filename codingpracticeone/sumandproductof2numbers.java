package com.greatlearning.codingpracticeone;
/*
Write a Java program to calculate the sum and product of two natural numbers
 */

public class sumandproductof2numbers {
    public static void main(String[] args) {
        int firstNumber, secondNumber;
        firstNumber = 20;
        secondNumber = 30;
        System.out.println("Sum is "+(firstNumber+secondNumber));
        System.out.println("Product is "+(firstNumber*secondNumber));
    }
}
