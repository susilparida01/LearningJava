package com.greatlearning.codingpracticetwo;
/*
Write a Java program to print the maximum of two numbers using ternary operator.
 */
public class MaximumOfTwoNumbers {
    public static void main(String[] args) {
        int input1,input2;
        input1 = 7;
        input2 = 34;
        System.out.println(input1>=input2?input1:input2);
    }
}
